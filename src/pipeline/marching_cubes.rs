//! Marching Cubes para extracao de isosuperficies a partir de mascaras 3D.
//!
//! Implementacao classica com tabela de 256 casos.
//! Ref: Lorensen & Cline, 1987.

use ndarray::Array3;
use tracing::info;

/// Extrai mesh para uma classe tumoral usando Marching Cubes.
///
/// Pipeline: mascara binaria -> suavizacao gaussiana -> upsample -> marching cubes -> normalizacao.
/// Retorna (vertices, faces) ou None se a classe tem poucos voxels.
#[allow(clippy::type_complexity)]
pub(crate) fn extract_mesh(
    mask: &Array3<u8>,
    class_id: u8,
    center: [f64; 3],
    scale: f64,
    upsample_factor: f64,
) -> Option<(Vec<[f32; 3]>, Vec<[u32; 3]>)> {
    let (h, w, d) = mask.dim();

    // 1. Mascara binaria float para a classe
    let mut binary = Array3::<f32>::zeros((h, w, d));
    for ((i, j, k), &v) in mask.indexed_iter() {
        if v == class_id {
            binary[[i, j, k]] = 1.0;
        }
    }

    // 2. Suavizacao gaussiana (sigma=0.8, separavel 3-pass)
    let smoothed = gaussian_smooth_3d(&binary, 0.8);

    // 3. Upsample se necessario (nearest-neighbor para manter volume coerente)
    let field = if (upsample_factor - 1.0).abs() > 0.01 {
        upsample_3d(&smoothed, upsample_factor)
    } else {
        smoothed
    };

    // 4. Marching Cubes (level = 0.5)
    let (mut verts, faces) = marching_cubes_impl(&field, 0.5);
    if verts.is_empty() {
        return None;
    }

    info!(
        class_id,
        vertices = verts.len(),
        faces = faces.len(),
        "marching cubes concluido"
    );

    // 5. Normalizar vertices: (v - center) / scale
    for v in &mut verts {
        v[0] = ((v[0] as f64 - center[0]) / scale) as f32;
        v[1] = ((v[1] as f64 - center[1]) / scale) as f32;
        v[2] = ((v[2] as f64 - center[2]) / scale) as f32;
    }

    Some((verts, faces))
}

// ---------------------------------------------------------------------------
// Gaussiana 3D separavel
// ---------------------------------------------------------------------------

fn gaussian_kernel_1d(sigma: f32) -> Vec<f32> {
    let radius = (3.0 * sigma).ceil() as usize;
    let size = 2 * radius + 1;
    let mut kernel = vec![0.0_f32; size];
    let s2 = 2.0 * sigma * sigma;
    let mut sum = 0.0_f32;
    for (i, kval) in kernel.iter_mut().enumerate().take(size) {
        let x = i as f32 - radius as f32;
        *kval = (-x * x / s2).exp();
        sum += *kval;
    }
    for v in &mut kernel {
        *v /= sum;
    }
    kernel
}

fn gaussian_smooth_3d(vol: &Array3<f32>, sigma: f32) -> Array3<f32> {
    let kernel = gaussian_kernel_1d(sigma);
    let r = kernel.len() / 2;
    let (h, w, d) = vol.dim();

    // Pass 1: suaviza ao longo do eixo 0 (linhas)
    let mut tmp1 = Array3::<f32>::zeros((h, w, d));
    for j in 0..w {
        for k in 0..d {
            for i in 0..h {
                let mut sum = 0.0_f32;
                for (ki, &kv) in kernel.iter().enumerate() {
                    let ii =
                        (i as isize + ki as isize - r as isize).clamp(0, h as isize - 1) as usize;
                    sum += vol[[ii, j, k]] * kv;
                }
                tmp1[[i, j, k]] = sum;
            }
        }
    }

    // Pass 2: suaviza ao longo do eixo 1 (colunas)
    let mut tmp2 = Array3::<f32>::zeros((h, w, d));
    for i in 0..h {
        for k in 0..d {
            for j in 0..w {
                let mut sum = 0.0_f32;
                for (ki, &kv) in kernel.iter().enumerate() {
                    let jj =
                        (j as isize + ki as isize - r as isize).clamp(0, w as isize - 1) as usize;
                    sum += tmp1[[i, jj, k]] * kv;
                }
                tmp2[[i, j, k]] = sum;
            }
        }
    }

    // Pass 3: suaviza ao longo do eixo 2 (profundidade)
    let mut result = Array3::<f32>::zeros((h, w, d));
    for i in 0..h {
        for j in 0..w {
            for k in 0..d {
                let mut sum = 0.0_f32;
                for (ki, &kv) in kernel.iter().enumerate() {
                    let kk =
                        (k as isize + ki as isize - r as isize).clamp(0, d as isize - 1) as usize;
                    sum += tmp2[[i, j, kk]] * kv;
                }
                result[[i, j, k]] = sum;
            }
        }
    }

    result
}

// ---------------------------------------------------------------------------
// Upsample 3D (nearest-neighbor linear para campo suavizado)
// ---------------------------------------------------------------------------

fn upsample_3d(vol: &Array3<f32>, factor: f64) -> Array3<f32> {
    let (h, w, d) = vol.dim();
    let nh = (h as f64 * factor).round() as usize;
    let nw = (w as f64 * factor).round() as usize;
    let nd = (d as f64 * factor).round() as usize;
    let mut out = Array3::<f32>::zeros((nh, nw, nd));

    for i in 0..nh {
        for j in 0..nw {
            for k in 0..nd {
                let si = ((i as f64 / factor) as usize).min(h - 1);
                let sj = ((j as f64 / factor) as usize).min(w - 1);
                let sk = ((k as f64 / factor) as usize).min(d - 1);
                out[[i, j, k]] = vol[[si, sj, sk]];
            }
        }
    }

    out
}

// ---------------------------------------------------------------------------
// Marching Cubes — implementacao classica com lookup tables
// ---------------------------------------------------------------------------

fn marching_cubes_impl(field: &Array3<f32>, level: f32) -> (Vec<[f32; 3]>, Vec<[u32; 3]>) {
    let (nx, ny, nz) = field.dim();
    if nx < 2 || ny < 2 || nz < 2 {
        return (Vec::new(), Vec::new());
    }

    let mut vertices: Vec<[f32; 3]> = Vec::new();
    let mut faces: Vec<[u32; 3]> = Vec::new();

    for i in 0..nx - 1 {
        for j in 0..ny - 1 {
            for k in 0..nz - 1 {
                // 8 vertices do cubo
                let vals = [
                    field[[i, j, k]],
                    field[[i + 1, j, k]],
                    field[[i + 1, j + 1, k]],
                    field[[i, j + 1, k]],
                    field[[i, j, k + 1]],
                    field[[i + 1, j, k + 1]],
                    field[[i + 1, j + 1, k + 1]],
                    field[[i, j + 1, k + 1]],
                ];

                // Indice na tabela: 8 bits, um por vertice
                let mut cube_index = 0_u8;
                for (vi, &val) in vals.iter().enumerate() {
                    if val >= level {
                        cube_index |= 1 << vi;
                    }
                }

                let edges = EDGE_TABLE[cube_index as usize];
                if edges == 0 {
                    continue;
                }

                // Posicoes dos 8 vertices do cubo
                let pos = [
                    [i as f32, j as f32, k as f32],
                    [(i + 1) as f32, j as f32, k as f32],
                    [(i + 1) as f32, (j + 1) as f32, k as f32],
                    [i as f32, (j + 1) as f32, k as f32],
                    [i as f32, j as f32, (k + 1) as f32],
                    [(i + 1) as f32, j as f32, (k + 1) as f32],
                    [(i + 1) as f32, (j + 1) as f32, (k + 1) as f32],
                    [i as f32, (j + 1) as f32, (k + 1) as f32],
                ];

                // Interpola vertices nas 12 arestas
                let mut edge_verts = [[0.0_f32; 3]; 12];
                for e in 0..12_u16 {
                    if edges & (1 << e) != 0 {
                        let (a, b) = EDGE_ENDPOINTS[e as usize];
                        edge_verts[e as usize] =
                            interpolate(pos[a], vals[a], pos[b], vals[b], level);
                    }
                }

                // Gera triangulos a partir da tabela
                let tri_row = &TRI_TABLE[cube_index as usize];
                let base = vertices.len() as u32;
                let mut local_verts = Vec::new();

                let mut ti = 0;
                while ti < tri_row.len() && tri_row[ti] != -1 {
                    let e0 = tri_row[ti] as usize;
                    let e1 = tri_row[ti + 1] as usize;
                    let e2 = tri_row[ti + 2] as usize;

                    // Adiciona vertices (com deduplicacao local simplificada)
                    let vi0 = add_vert(&mut local_verts, edge_verts[e0]);
                    let vi1 = add_vert(&mut local_verts, edge_verts[e1]);
                    let vi2 = add_vert(&mut local_verts, edge_verts[e2]);

                    faces.push([base + vi0 as u32, base + vi1 as u32, base + vi2 as u32]);
                    ti += 3;
                }

                vertices.extend_from_slice(&local_verts);
            }
        }
    }

    (vertices, faces)
}

fn add_vert(verts: &mut Vec<[f32; 3]>, v: [f32; 3]) -> usize {
    let idx = verts.len();
    verts.push(v);
    idx
}

fn interpolate(p1: [f32; 3], v1: f32, p2: [f32; 3], v2: f32, level: f32) -> [f32; 3] {
    if (v1 - v2).abs() < 1e-10 {
        return p1;
    }
    let t = (level - v1) / (v2 - v1);
    [
        p1[0] + t * (p2[0] - p1[0]),
        p1[1] + t * (p2[1] - p1[1]),
        p1[2] + t * (p2[2] - p1[2]),
    ]
}

